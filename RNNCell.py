numbers = [1, 123, 34]
for number in numbers:
    if len(str(number)) == 1:
        print(number, "는 {} 자릿수입니다.".format(len(str(number))))
    elif len(str(number)) == 2:
        print(number, "는 {} 자릿수입니다.".format(len(str(number))))
    else:
        print(number, "는 {} 자릿수입니다.".format(len(str(number))))
